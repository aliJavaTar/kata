package com.data_structure.bit;

public class Calculate {
    public int calculateBinaryOrOperate(int firstNumber, int secondNumber) {
        return firstNumber | secondNumber;
    }

    public int calculateBinaryAndOperate(int firstNumber, int secondNumber) {
        return firstNumber & secondNumber;
    }

    public int calculateBinaryShiftLeftOperate(int firstNumber, int secondNumber) {
        return firstNumber << secondNumber;
    }
}
